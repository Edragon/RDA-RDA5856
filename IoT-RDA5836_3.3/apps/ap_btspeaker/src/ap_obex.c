static void _GetPinyin(UINT16 Char, UINT8 Pinyin[MAX_PINYIN_LEN+1])
{
    UINT32 Low = 0;
    UINT32 High = TOTAL_CHINESE_WORD_COUNT;
    UINT32 Mid = 0;

    if(Char == 0x0000)
    {
        return;
    }

    if (Char >= 0x20 && Char <= 0x7e)// support searching mix name including English or digit
    {
       UINT16 vChar = Char;
       if (Char >= L'A' && Char <= L'Z')
       {
          vChar = Char + 'a' - 'A';//towlower(Char);
       }
       Pinyin[0] = (UINT8)(vChar);
       Pinyin[1] = '\0';
       return;
    }
    
    if(Char < 19968 || Char > 40864)
    {
        Pinyin[0] = '\0';
        return;
    }
    
    while (Low <= High)
    {
        Mid = (Low + High)/2;
        if(_gUCS2PY[Mid].Word == Char)
        {
            strncat(Pinyin, _gUCS2PY[Mid].Pinyin, 6);
            return;
        }
        else if (Char < _gUCS2PY[Mid].Word)
        {
            High = Mid - 1;
        }
        else
        {
            Low = Mid +1;
        }
    }

    Pinyin[0] = '\0';

}

UINT32 get_pinyin_by_unicode_string(const UINT16* pUniStr, UINT8 Pinyin[MAX_PINYIN_COUNT][MAX_PINYIN_LEN+1])
{
    UINT32 vIndex = 0;
    if(pUniStr == NULL || Pinyin == NULL)
        return 0;
    
    while(*pUniStr && vIndex < MAX_PINYIN_COUNT)
    {
        _GetPinyin(*pUniStr, Pinyin[vIndex]);
        pUniStr++;
        vIndex++;
    }

	return vIndex;
}

